package com.company;

    public class TreeNode {
        public char data;
        public TreeNode left;
        public TreeNode right;

        public TreeNode(char data) {
            this(data, null, null);
        }
        public TreeNode(char data, TreeNode left, TreeNode right) {
            this.data = data;
            this.left = left;
            this.right = right;
        }





    }
